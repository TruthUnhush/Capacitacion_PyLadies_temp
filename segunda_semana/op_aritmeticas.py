a = 10
b = 5
c = 2.5
d = 300
e = 12.34
f = 2
g = 12

# Sumando variables
print(a + b)

# Restando variables
print(a - b)

# Dividiendo variables
print(a / b)

# Multiplicando variables
print(a * b)

# Exponenciación de variables

print(c * c) 
print(c ** f)  # Esto es igual a 2.5 elevado a 2 

# División entera

print(d / e)  # División regular
print(d // e)  # División entera

# Módulo

print(g / b)
print(g % b)

